#include<iostream>
using namespace std;
int main()
{
    int w;
    
    cin>>w;
    if(w%2==1)
    {
        cout<<"NO"<< endl;
    }else
    {
        if(2==w)
        {
            cout<<"No"<< endl;
        }else
        {
            cout<<"YES"<< endl;
        }
    }
    return 0;
}